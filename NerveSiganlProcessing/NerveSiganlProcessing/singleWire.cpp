#include "singleWire.h"

///-------------------------------------------------------------------------------------------------
/// <summary>	Default constructor. </summary>
///-------------------------------------------------------------------------------------------------
singleWire::singleWire() {}

///-------------------------------------------------------------------------------------------------
/// <summary>	Print single wire location. </summary>
///-------------------------------------------------------------------------------------------------
void singleWire::printSingleWireLocation() {
	cout << "(" << wireLocation.coorX << ", " << wireLocation.coorY << ", " << wireLocation.coorZ << ");";
}

///-------------------------------------------------------------------------------------------------
/// <summary>	Print single result location. </summary>
///-------------------------------------------------------------------------------------------------
void singleWire::printSingleResultLocation() {
	cout << "(" << resultLocation.coorX << ", " << resultLocation.coorY << ", " << resultLocation.coorZ << ");";
}

///-------------------------------------------------------------------------------------------------
/// <summary>	Print received signal. </summary>
///-------------------------------------------------------------------------------------------------
void singleWire::printReceivedSignal() {
	cout << "\nReceived signal" << endl;
	for (auto &pulse : pulses) {
		cout << pulse.receivedLength << "\t";
	}
}
